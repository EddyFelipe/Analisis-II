    private void Loguear()
    {
        try {
            if (jTextField1.getText().length() == 0 || jPasswordField1.getPassword().length == 0)
                JOptionPane.showMessageDialog(null, "Por favor rellene los campos de usuario y contrase単a");
            else
            {
                empleado = clasEmpleados.buscarUsuario(singleton.getConnection(), jTextField1.getText());
                if (empleado != null)
                {
                    if (contrasena.Desencriptar(empleado.getContrasena()).equals(String.valueOf(jPasswordField1.getPassword())))
                    {
                        fgeneral aplicacion = new fgeneral();
                        aplicacion.empleado = this.empleado;
                        aplicacion.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                        JOptionPane.showMessageDialog(null, "Usuario o contrase単a incorrectos");
                }
                else
                    JOptionPane.showMessageDialog(null, "Usuario o contrase単a incorrectos");
            }
        } catch (Exception ex) {
           Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
